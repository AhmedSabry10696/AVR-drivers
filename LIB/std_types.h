#ifndef STDTYPES_H_
#define STDTYPES_H_

/****************************************************************/
/************************ Null Pointer **************************/
/****************************************************************/
#define NULLPTR ((void *)0)

/****************************************************************/
/********************* Unsigned Data Types **********************/
/****************************************************************/
typedef unsigned char u8;
typedef unsigned short u16;
typedef unsigned long u32;
typedef unsigned long long u64;

/****************************************************************/
/*********************** Signed Data Types **********************/
/****************************************************************/
typedef signed char s8;
typedef signed short s16;
typedef signed long s32;
typedef signed long long s64;

/****************************************************************/
/************************ Float Data Types **********************/
/****************************************************************/
typedef float f32;
typedef double f64;

/****************************************************************/
/********************** Boolean Data Types **********************/
/****************************************************************/
typedef enum
{
	FALSE = 0,
	TRUE = 1
} Bool_type;

/****************************************************************/
/********************* Register Data Types **********************/
/****************************************************************/
typedef union
{
	u8 data;
	struct
	{
		u8 Bit0 : 1;
		u8 Bit1 : 1;
		u8 Bit2 : 1;
		u8 Bit3 : 1;
		u8 Bit4 : 1;
		u8 Bit5 : 1;
		u8 Bit6 : 1;
		u8 Bit7 : 1;
	} Bits; 
	/* struct Bits share the same values that is in u8 data var */
} Reg_8bit;

/****************************************************************/
/********************* End of Stdandard Types *******************/
/****************************************************************/

#endif /* STDTYPES_H_ */